###################################################################################################################################################################
import os
from datetime import datetime

if __name__ == "__main__":
    now = datetime.now()
    current_time = now.strftime("%H%M")
    cmd1 = "top -u '!root'  -b -n 1 | sed -n '8, 120{s/^ *//;s/ *$//;s/  */,/gp;};120q' > D%s.csv" %current_time  # omits the root data and usage
    os.system(cmd1)
